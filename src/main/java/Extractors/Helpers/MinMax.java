package Extractors.Helpers;

public class MinMax
{
    private double min = 20000000.0;
    private double max = -200000000.0;

    public double getMin()
    {
        return min;
    }

    public void setMin(double min)
    {
        this.min = min;
    }

    public double getMax()
    {
        return max;
    }

    public void setMax(double max)
    {
        this.max = max;
    }
}
